/*-------------------------------------------------------------------
 *  nfs_ecc_calc_other
 *-------------------------------------------------------------------*/

void nfs_ecc_calc_other(const nfs_u8 *data, nfs_u32 n_bytes,
                        nfs_ecc_other_t *ecc)
{
    nfs_u32  i;
    nfs_u8   col;
    nfs_u32  line_parity       = 0;
    nfs_u32  line_parity_prime = 0;

    ecc->col_parity = 0;

    for (i = 0; i < n_bytes; i++) {
        col = column_parity_table[data[i]];
        ecc->col_parity ^= col;

        if (col & 1) {
            /* odd column parity: this byte contributes to line parity */
            line_parity       ^= i;
            line_parity_prime ^= ~i;
        }
    }

    ecc->line_parity       = line_parity;
    ecc->line_parity_prime = line_parity_prime;
}

/*-------------------------------------------------------------------
 *  nfs_ecc_correct_other
 *-------------------------------------------------------------------*/

int nfs_ecc_correct_other(nfs_u8 *data, nfs_u32 n_bytes,
                          nfs_ecc_other_t *read_ecc,
                          const nfs_ecc_other_t *calc_ecc)
{
    nfs_u8  diff_col;
    nfs_u32 diff_line;
    nfs_u32 diff_line_prime;
    nfs_u32 bit;

    diff_col        = read_ecc->col_parity       ^ calc_ecc->col_parity;
    diff_line       = read_ecc->line_parity       ^ calc_ecc->line_parity;
    diff_line_prime = read_ecc->line_parity_prime ^ calc_ecc->line_parity_prime;

    if (!diff_col && !diff_line && !diff_line_prime)
        return 0;   /* no error */

    if ((diff_line ^ diff_line_prime) == (nfs_u32)(n_bytes - 1)) {
        /* Single-bit error: locate the bit */
        bit  = 0;
        if (diff_col & 0x02) bit |= 1;
        if (diff_col & 0x08) bit |= 2;
        if (diff_col & 0x20) bit |= 4;

        /* Correct in place and update stored ECC */
        data[diff_line] ^= (nfs_u8)(1 << bit);
        read_ecc->col_parity       = calc_ecc->col_parity;
        read_ecc->line_parity      = calc_ecc->line_parity;
        read_ecc->line_parity_prime= calc_ecc->line_parity_prime;
        return 1;   /* corrected */
    }

    /* Uncorrectable */
    return -1;
}
