/*
 * nfs_ecc.c — Software Hamming ECC for NFS
 *
 * Ported from yaffs_ecc.c (YAFFS2).  The column parity table covers
 * 8 columns of a byte; line parity accumulates over all bytes.
 * Detects and corrects 1-bit errors; detects (but does not correct)
 * 2-bit errors.
 */

